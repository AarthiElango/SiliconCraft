function setupImageClickListeners(containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;
  
  const carouselImages = container.querySelectorAll('.carousel-item img');
  const lightboxModal = new bootstrap.Modal(document.getElementById('imageLightboxModal'));
  const lightboxImage = document.getElementById('lightboxImage');
  
  carouselImages.forEach(img => {
    img.addEventListener('click', function() {
      const src = this.src;
      lightboxImage.src = src;
      lightboxModal.show();
    });
  });
}
function setupImageLoading() {
  // Handle lazy loading for all images
  const images = document.querySelectorAll('img[loading="lazy"]');
  
  if ('IntersectionObserver' in window) {
    const imageObserver = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          const img = entry.target;
          loadImage(img);
          observer.unobserve(img);
        }
      });
    }, {
      rootMargin: '50px' // Start loading 50px before image enters viewport
    });

    images.forEach(img => imageObserver.observe(img));
  } else {
    // Fallback for browsers without IntersectionObserver
    images.forEach(img => loadImage(img));
  }
}

function loadImage(img) {
  const src = img.getAttribute('data-src') || img.src;
  if (!src) return;

  // Add loading class
  img.classList.add('image-loading');
  
  // Create a new image to preload
  const tempImg = new Image();
  
  tempImg.onload = function() {
    img.src = src;
    img.classList.remove('image-loading');
    img.classList.add('loaded', 'fade-in');
  };
  
  tempImg.onerror = function() {
    img.classList.remove('image-loading');
    img.classList.add('error');
    console.error('Failed to load image:', src);
  };
  
  tempImg.src = src;
}

// Handle image errors globally
function setupImageErrorHandling() {
  document.addEventListener('error', function(e) {
    if (e.target.tagName === 'IMG') {
      e.target.classList.add('error');
      console.error('Image failed to load:', e.target.src);
    }
  }, true);
}

// Update the openLightbox function
function openLightbox(imageSrc) {
  const lightboxImage = document.getElementById("lightboxImage");
  const lightbox = document.getElementById("lightbox");

  if (lightboxImage && lightbox) {
    // Show loading state
    lightboxImage.classList.add('image-loading');
    lightboxImage.src = '';
    
    // Preload image
    const tempImg = new Image();
    tempImg.onload = function() {
      lightboxImage.src = imageSrc;
      lightboxImage.classList.remove('image-loading');
      lightboxImage.classList.add('fade-in');
    };
    
    tempImg.onerror = function() {
      lightboxImage.classList.remove('image-loading');
      lightboxImage.classList.add('error');
      console.error('Failed to load lightbox image:', imageSrc);
    };
    
    tempImg.src = imageSrc;
    
    lightbox.classList.remove("hidden");
    document.body.classList.add('lightbox-open');
  }
}

// Update the closeLightbox function
function closeLightbox() {
  const lightbox = document.getElementById("lightbox");
  const lightboxImage = document.getElementById("lightboxImage");
  
  if (lightbox) {
    lightbox.classList.add("hidden");
    document.body.classList.remove('lightbox-open');
    
    // Clear image after animation
    setTimeout(() => {
      if (lightboxImage) {
        lightboxImage.src = '';
        lightboxImage.classList.remove('fade-in', 'error');
      }
    }, 300);
  }
}


function showDetail(category) {
  isDetailViewVisible = true;
  pauseAllMainCardAutoplay();

  const data = mouData[category];

  document.getElementById("homeView").classList.add("hidden");
  document.getElementById("detailView").classList.remove("hidden");

  const titleEl = document.getElementById("detailTitle");
  const iconEl = document.getElementById("detailIconSvg");

  if (titleEl) titleEl.textContent = data.title;
  if (iconEl) iconEl.innerHTML = data.icon;

  const grid = document.getElementById("detailGrid");
  if (grid) {
    grid.innerHTML = "";

    data.cards.forEach((card) => {
      currentDetailSliderIndices[card.id] = 0;

      const cardElement = document.createElement("div");
      cardElement.className = "detail-card";
      cardElement.setAttribute("data-card-id", card.id);
      cardElement.innerHTML = generateDetailCardHTML(card);

      grid.appendChild(cardElement);
    });

    setupDetailCardEventListeners();
    setupImageLoading(); // Load images for detail view
    
    setTimeout(() => {
      setupDetailCardAutoplay();
    }, 100);
  }
}